package com.example.zhangyiwei.mycoolweather;

import android.app.Fragment;
import android.app.IntentService;
import android.app.ProgressDialog;
import android.content.Intent;
import android.os.Build;
import android.os.Bundle;
import android.support.annotation.Nullable;
import android.support.annotation.RequiresApi;
import android.view.LayoutInflater;
import android.view.View;
import android.view.ViewGroup;
import android.widget.AdapterView;
import android.widget.ArrayAdapter;
import android.widget.Button;
import android.widget.ListView;
import android.widget.TextView;
import android.widget.Toast;

import com.example.zhangyiwei.mycoolweather.db.City;
import com.example.zhangyiwei.mycoolweather.db.County;
import com.example.zhangyiwei.mycoolweather.db.Province;
import com.example.zhangyiwei.mycoolweather.gson.Weather;
import com.example.zhangyiwei.mycoolweather.util.HttpUtil;
import com.example.zhangyiwei.mycoolweather.util.Utility;

import org.litepal.crud.DataSupport;

import java.io.IOException;
import java.nio.file.attribute.PosixFileAttributes;
